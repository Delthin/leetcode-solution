    long long sum(int first, int last, int n) {
        return (first + last) * n / 2;
    }
public:
    int differenceOfSums(int n, int m) {
        long long num2 = sum(0, (n / m) * m, n / m + 1);
        long long num1 = sum(1, n, n);
        return num1 - 2 * num2;
    }
