class Solution {
public:
    int trap(vector<int>& height) {
        stack<int> st;
        int ans = 0;
        for (int i = 0; i < height.size(); i++) {
            while (!st.empty() && height[i] > height[st.top()]) {
                int bottom_idx = st.top();
                st.pop();
                if (!st.empty()) {
                    int h = min(height[st.top()], height[i]) - height[bottom_idx];
                    int w = i - st.top() - 1;
                    ans += h * w;
                }
            }
            st.emplace(i);
        }
        return ans;
    }
};
